#count - retorna o número de ocorrências de um elemento na lista
lista1=['a', 'b', 'c']
lista1.count('d')
print(lista1)

#remove - remove um elemento da lista a partir do seu valor
lista2=['a', 'b', 'c']
lista2.remove('c')
print(lista2)

#sort - ordena a lista numericamente ou alfabeticamente
lista3=['d', 'c', 'e', 'b', 'a', 'f']
lista3.sort()
print(lista3)

#reverse - inverte as posições dos elementos da lista
lista4=['a', 'b', 'c', 'd', 'e']
lista4.reverse()
print(lista4)

#append
lista5=['a', 'b', 'c']
lista5.append('d')
print(lista5)

#extend
lista6=['a', 'b', 'c']
lista6_1=['d', 'e']
lista6.extend(lista6_1)
print(lista6)

#insert
lista7=['a', 'b', 'c']
lista7.insert(0, 'd')
print(lista7)

